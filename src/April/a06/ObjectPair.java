package April.a06;

public class ObjectPair {
	
	Object first;
	Object second;
	public Object getFirst() {
		return first;
	}

	public void setFirst(Object first) {
		this.first = first;
	}

	public Object getSecond() {
		return second;
	}

	public void setSecond(Object second) {
		this.second = second;
	}

	
	
	public ObjectPair(Object a, Object b) {
		first = a;
		second = b;
	}

}
